#include <unistd.h>

char	*ft_strncat(char *dest, char *src, unsigned int nb)
{
	nt				a;
	unsigned int	b;

	a = 0;
	while (dest[a])
	{
		a++;
	}
	b = 0;
	while (src[b] && b < nb)
	{
		dest[a] = src[b];
		a++;
		b++;
	}
	dest[a] = '\0';
	return (dest);
}
