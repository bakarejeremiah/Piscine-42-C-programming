#include <unistd.h>

unsigned int	ft_strlcpy(char *dest, char *src, unsigned int size)
{
	unsigned int	i;

	i = 0;
	while (src[i])
	{
		if (i < size - 1)
			dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (i);
}

#include <stdio.h>
int    main()
{
    char src[] = "what is going on";
    char dest[] = "Jeremiah";
    printf("%d | %s", ft_strlcpy(dest, src, 24), dest);
}